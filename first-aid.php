                    <h2>What is First Aid?</h2>
                </div>

                <div class="content">
                    <p>&nbsp; &nbsp; &nbsp; &nbsp; First aid refers to the initial assistance or treatment provided to someone who has been injured or becomes ill unexpectedly.
                    First aid may be sufficient for minor ailments. First aid should be maintained for serious problems until more advanced care is available.</p>

                    <p>&nbsp; &nbsp; &nbsp; &nbsp; The ability to provide appropriate first aid can mean the difference between life and death. Begin by introducing yourself as a first-aid provider eager to assist the injured or ill individual. You must have the person's permission to assist them; do not touch them until they agree to be helped. If you come across a confused person or someone who is critically injured or ill, you can assume they want you to assist them. This is referred to as "implied consent."</p>

                    <p>&nbsp; &nbsp; &nbsp; &nbsp; Before approaching a casualty, make sure that the area is safe for you. In hazardous situations, do not attempt heroic rescues. If you put yourself in danger, you will be unable to assist casualties and may become one yourself, causing harm to others. If it is unsafe to approach the casualty, call 911 for emergency assistance.</p>

                    <h3>First Aid Method of DRSABCD</h3>

                    <p>&nbsp; &nbsp; &nbsp; &nbsp; The ABCs of first aid is a mnemonic that stands for <span>airway</span>, <span>breathing</span>, and <span>circulation</span>. In any situation, apply the <span class="drsabcd">DRSABCD</span>  Action Plan.</p>

                    <p><span class="drsabcd">DRSABCD</span> stands for :</p>

                    <div class="list">
                        <ul>
                            <li><span class="abc">DANGER :</span> Always check for potential dangers.</li>
                            <li><span class="abc">RESPONSE :</span> <span class="quest">Is the person conscious? Do they respond when you talk to them, touch their hands or squeeze their shoulder? &nbsp;</span></li>
                            <li><span class="abc">SEND FOR HELP :</span> Check RescueMe's <span class="quest">"Ambulance Request"</span> and send an emergency request.</li>
                            <li><span class="abc">AIRWAY :</span> <span class="quest">Is the airway obstructed?</span>&nbsp; Use measures to clear the airway.</li>
                            <li><span class="abc">BREATHING :</span> <span class="quest">Is the person breathing?</span>&nbsp; Start rescue breathing.</li>
                            <li><span class="abc">CIRCULATION : </span> <span class="quest">Is the person's heart beating?</span>&nbsp; Start a chest compression.</li>
                            <li><span class="abc">DEFIBRILLATOR :</span> For unconscious adults who are not breathing, apply an automated external defribilator (AED) if one is available.</li>
                        </ul>
                    </div>

                    <h3>What is CPR?</h3>
                    <p>&nbsp; &nbsp; &nbsp; &nbsp; When someone is not breathing or their heart is not beating, CPR is a life-saving technique that is used.</p>
                    <p>&nbsp; &nbsp; &nbsp; &nbsp; Rescue breathing and chest compressions, also known as CPR (cardiopulmonary resuscitation), are used to revive a victim (mouth-to-mouth resuscitation). CPR can help re-start breathing and get oxygen-rich blood to the brain in cases where someone isn't breathing or circulating (moving) blood well.</p>

                    <h3>CPR Process</h3>
                    <p>&nbsp; &nbsp; &nbsp; &nbsp; One cycle consists of 30 chest compressions followed by 2 rescue breaths. Be careful not to breathe too quickly or with too much force. Resuming chest compressions will help the blood flow. As soon as an automated external defibrillator (AED) is available, use it and adhere to the instructions.</p>

                    <h3>Where to Learn First Aid and CPR?</h3>
                    <p>&nbsp; &nbsp; &nbsp; &nbsp; The information compiled throughout this application will assist you in providing effective first aid to any casualty in any situation. However, to become a fully qualified first responder, you must complete a recognized first aid course and obtain certification. You can start by taking a CPR or first aid course through a non-profit organization such as the Philippine Red Cross. This will also help to improve your skills and boost your confidence.</p>
